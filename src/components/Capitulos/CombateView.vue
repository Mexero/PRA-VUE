            <p>
                Muchos movimientos y otros efectos similares dicen que una criatura
                <strong>recibe daño</strong>, recibir daño equivale a sustraer la
                cantidad de daño recibido de los PV actuales que tiene una criatura. Si
                un efecto multiplica o divide el daño y otro sustrae o añade una
                cantidad a este, primero se calculan las cantidades añadidas y
                sustraídas y luego se multiplica o divide el daño.
            </p>

            <p>Cuando una criatura llega a 0 PV, queda Inconsciente.</p>
        </div>
    </details>

