<template>
    <h1>Capítulo 7: Combate</h1>

    <p>
        Los combates son unos de los principales atractivos del Mundo Pokémon y no
        es de extrañar que sean una de las mecánicas que más se utilicen a lo largo
        de vuestras campañas. Este capítulo explicará cómo funcionan los combates y
        qué puede hacerse durante estos.
    </p>

    <details open id="apartado1">
        <summary>La Iniciativa</summary>
        <div class="detailsBloque">
            <p>
                Aunque en un combate todos los involucrados tomarían sus acciones de
                forma simultánea, para simplificar estas situaciones haremos uso de la
                <strong>Iniciativa</strong>. La Iniciativa es una lista que indica el
                orden de los turnos de los participantes en el combate. Para determinar
                el orden de la Iniciativa, al inicio del combate, o cuando se unan a la
                misma, los participantes hacen una Tirada de Habilidad de Iniciativa y
                luego estas se ordenan de forma descendente. Si dos participantes de
                bandos distintos empatan, el que tenga mayor Agilidad elige si va
                primero o último. Además, si un grupo de participantes de la Iniciativa
                que pertenecen a un mismo bando (es decir, que trabajan como equipo)
                toman sus iniciativas consecutivamente, pueden tomar sus turnos
                conjuntamente.
            </p>

            <p>
                Al inicio del combate, todos los entrenadores que no están
                <strong>Sorprendidos</strong> pueden sacar un Pokémon a la vez y pueden
                sacar un arma de forma gratuita. Los Pokémon que tienen un entrenador
                toman su turno a la vez que su entrenador y, si este llegase a quedar
                fuera de combate, toman su turno libremente cuando lo hubiese tomado su
                entrenador.
            </p>
        </div>
    </details>

    <details open id="apartado2">
        <summary>Los Turnos</summary>
        <div class="detailsBloque">
            <p>
                Cada participante del combate tiene durante un turno una
                <strong>Acción</strong>, una <strong>Interacción</strong>,
                <strong>Movimiento</strong> y una <strong>Reacción</strong>. Además, un
                entrenador y su Pokémon comparten una
                <strong>Acción Compartida</strong>.
            </p>

            <p>
                Las <strong>Acciones</strong> suelen ser la parte más importante de los
                turnos, pues la mayoría de Movimientos y Rasgos suelen consumirlas.
                Algunos otros ejemplos de Acciones que pueden usarse en combate son:
            </p>

            <ul>
                <li>
                    <p>
                        Hacer una tirada de Combate (Fuerza) para hacer un Agarre (Ver la
                        sección de “Agarrar” en el Capítulo 10. Glosario de Reglas Menores).
                    </p>
                </li>

                <li>
                    <p>
                        Hacer una tirada de Combate (Fuerza) para Empujar. (Ver la sección
                        de “Empujar” en el Capítulo 10. Glosario de Reglas Menores).
                    </p>
                </li>

                <li>
                    <p>Lanzar una Poké Ball.</p>
                </li>

                <li>
                    <p>Usar la Pokédex para obtener información de un Pokémon.</p>
                </li>

                <li>
                    <p>Usar una Acción Interacción.</p>
                </li>

                <li>
                    <p>
                        Tomar la acción de <strong>Correr</strong>, que hace que todas las
                        velocidades se dupliquen hasta el final del turno.
                    </p>
                </li>

                <li>
                    <p>
                        Tomar la acción de <strong>Destrabarse</strong>, que hace que el
                        movimiento durante este turno no genere Ataques de Oportunidad.
                    </p>
                </li>
            </ul>

            <p>
                Las <strong>Acciones Compartidas</strong> son acciones que entrenador y
                Pokémon hacen en sintonía. Usualmente las acciones compartidas se usan
                para:
            </p>
            <ul>
                <li>
                    <p>Dar una orden a un Pokémon que controlas y que este la reciba.</p>
                </li>
                <li>
                    <p>Montar o desmontar a un Pokémon.</p>
                </li>
                <li>
                    <p>Devolver, cambiar o sacar a un Pokémon de su Poké Ball.</p>
                </li>
            </ul>

            <p>
                El <strong>Movimiento</strong> permite a una criatura usar sus
                Velocidades. Una criatura puede moverse un máximo de metros durante su
                turno igual a la Velocidad de la que hace uso. Si, durante un turno,
                primero se hace uso de un tipo de Velocidad y luego de otro, el
                movimiento total en metros no puede superar la Velocidad del último tipo
                de movimiento que se ha usado.
            </p>

            <p>
                La <strong>Interacción</strong> es un tipo de acción que se usa para
                interactuar con el entorno, ya sea para decir algo a alguien, abrir o
                cerrar una puerta, tomar un objeto que está tirado por el suelo u otro
                tipo de acciones similares.
            </p>

            <p>
                La <strong>Reacción</strong> es la única parte de un turno que puede
                usarse fuera del propio turno. Todas las Reacciones tienen una condición
                que te dice cuando puedes usarlas. La reacción más común es hacer un
                Ataque de Oportunidad. Una criatura genera un Ataque de Oportunidad
                cuando sale de tu rango de cuerpo a cuerpo haciendo uso de su
                movimiento. Estos Ataques de Oportunidad te permiten realizar un
                Movimiento que conozcas y puedas usar sin consumir PP que haga
                exactamente una Tirada de Ataque Cuerpo a Cuerpo contra la criatura que
                generó el Ataque de Oportunidad.
            </p>

            <div class="consejoCapitulo">
                <p>
                    <strong>Nota:</strong> Un Pokémon que está en su Poké Ball no toma
                    turnos, por lo que los efectos que piden repetir salvaciones al final
                    de los turnos no acaban hasta que finalice su duración.
                </p>
            </div>
        </div>
    </details>

    <details open id="apartado3">
        <summary>Preparar Acción</summary>
        <div class="detailsBloque">
            <p>
                En ciertas ocasiones, querrás esperar a que ocurra algo antes de usar
                una de tus Acciones. Cuando esto ocurre, gastas la Acción que te
                preparas para usar y declaras un evento para el que te preparas. Hasta
                el principio de tu próximo turno, cuando ocurra el evento descrito,
                puedes usar tu Reacción para usar la Acción que preparaste.
            </p>

            <p>
                Tanto Pokémon como entrenadores pueden preparar sus acciones y si usar
                una acción que preparas consume un recurso, como los PP, este no se
                gasta hasta que se lanza como Reacción.
            </p>
        </div>
    </details>

    <details open id="apartado4">
        <summary>Dar Órdenes</summary>
        <div class="detailsBloque">
            <p>
                Dar una <strong>Orden</strong> a un Pokémon que controlas es una Acción
                Compartida. Además, sólo puede darse una Orden por turno y a una
                criatura a menos de 20 metros de ti.
            </p>

            <p>
                Estas órdenes incluyen todas las acciones y el movimiento que un Pokémon
                toma durante su turno, excepto las Reacciones, que puede tomar por su
                cuenta.
            </p>

            <p>
                Un Pokémon con su entrenador consciente que no recibe una Orden no hace
                nada durante su turno, salvo moverse si hay un peligro inminente.
            </p>
        </div>
    </details>

    <details open id="apartado5">
        <summary>Cambiar de Pokémon</summary>
        <div class="detailsBloque">
            <p>
                Un entrenador puede <strong>Cambiar</strong> a su Pokémon devolviéndolo
                a la Poké Ball para sacar a otro. Cambiar de Pokémon tiene un Coste de
                Acción de Acción Compartida y, además, el Pokémon que sale no puede
                recibir Órdenes hasta el principio de su próximo turno. Para poder
                Cambiar un Pokémon este debe de estar a menos de 10 metros de su
                entrenador y el Pokémon que sale puede sacarse a una distancia máxima de
                5 metros. Además, Como parte de la Acción Compartida de Cambiar a un
                Pokémon, este último puede usar su Movimiento para acercarse a ti hasta
                estar a rango de ser cambiado.
            </p>

            <p>
                Un Pokémon que es devuelto a su Poké Ball termina su turno y, además,
                todos los efectos que solo afectan a este y acaban al final de la Escena
                o dependen de su posición terminan prematuramente. Los Niveles de los
                Estados Nivelados no se pierden al volver a la Poké Ball, pero si que se
                reducen en 1 porque el Pokémon termina su turno.
            </p>

            <p>
                Una excepción a la regla de cambiar Pokémon ocurre cuando un Pokémon cae
                a 0 PV. En este caso, el entrenador puede devolverlo a la Poké Ball de
                forma gratuita e instantáneamente, independientemente de la distancia
                entre ambos y, en su próximo turno, sacar otro Pokémon de forma
                gratuita, al cual puede dar una Orden en ese mismo turno.
            </p>

            <p>
                Siempre que un entrenador pueda Cambiar a su Pokémon también puede
                <strong>Retirarlo</strong> con su Acción Compartida. La única diferencia
                entre estas dos Acciones es que el entrenador no saca otro Pokémon tras
                devolver al primero a su Poké Ball.
            </p>

            <p>
                Por último, un entrenador puede querer <strong>Sacar</strong> un Pokémon
                cuando no tiene a ningún otro fuera de la Poké Ball. Al principio de la
                Iniciativa, cuando un entrenador se une a esta o durante el primer turno
                en el que no está Sorprendido, un entrenador puede Sacar un Pokémon sin
                ningún coste de acciones ni restricciones pero, en el resto de casos,
                hacerlo cuesta una Acción Compartida y el Pokémon sacado no puede
                recibir Órdenes esa Ronda.
            </p>
        </div>
    </details>

    <details open id="apartado6">
        <summary>Tipos de Ataques</summary>
        <div class="detailsBloque">
            <p>
                Un Rasgo o Movimiento puede describir que haces un ataque de cierto
                tipo. Los ataques cuerpo a cuerpo sólo pueden realizarse contra
                criaturas a 1 metro de ti, a no ser que se describa lo contrario. Los
                ataques a rango siempre tienen una distancia máxima a la que pueden
                hacerse.
            </p>

            <p>
                Para realizar un ataque, haces una Tirada de Ataque contra la CA del
                objetivo. Si la superas o igualas, se considera que el ataque golpea.
            </p>
        </div>
    </details>

    <details open id="apartado7">
        <summary>Usar una Cuadrícula</summary>
        <div class="detailsBloque">
            <p>
                Para hacer más visual un combate, el DJ puede decidir usar una
                cuadrícula. Cuando se hace uso de una de estas, cada cuadrado representa
                1 metro cuadrado de distancia, por lo que las criaturas de tamaño
                Mediano o inferior ocupan una casilla, las Grandes ocupan un cuadrado de
                2 casillas de lado, las Enormes 3 un cuadrado de 3 casillas de lado y
                las Colosales un cuadrado de tantas casillas de lado como midan.
            </p>

            <p>
                Hay dos formas de medir las distancias en las cuadrículas, la Simple o
                la Realista. La forma Simple asume que moverse de una casilla a otra
                casilla adyacente cuesta 1 metro de movimiento. La forma Realista
                requiere de usar una regla que mida la distancia del recorrido que
                quiere hacerse.
            </p>

            <p>
                Cuando se usa una cuadrícula, las criaturas de tamaño Mediano o superior
                bloquean el paso a sus rivales por las casillas que ocupan y sus aliados
                pueden cruzarlas como Terreno Difícil
            </p>
        </div>
    </details>

