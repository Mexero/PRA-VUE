            <details class="subDetails">
                <summary class="subSummary">Recompensas</summary>
                <div class="detailsBloque">
                    <p>
                        Cuando los jugadores ayuden a alguien o terminen un trabajo es
                        natural que reciban una recompensa. Esta puede ser monetaria u
                        objetos de valor, como un Amuleto o un mapa, además de información.
                    </p>

                    <p>
                        Cuando la recompensa sea monetaria, piensa en cuánto tiempo están
                        dedicando los personajes para completar el trabajo, así como la
                        dificultad del mismo y considera cuántos recursos van a gastar para
                        poder completarlo, pues al final del día al terminar un encargo
                        según lo previsto los personajes deberían tener más dinero que
                        cuando lo empiezan.
                    </p>

                    <p>
                        Cuando la recompensa sean objetos, debes pensar en el valor que
                        tienen estos y, sobre todo, la rareza de los mismos. Una buena idea
                        es que cuando das recompensas en forma de objeto estas tengan una
                        relación con el contexto en el que se consiguen.
                    </p>

                    <p>
                        Para más información, puedes guiarte con la sección de este mismo
                        capítulo, Los Tiers de Juego.
