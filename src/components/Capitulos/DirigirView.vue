                        km que te desplazas se multiplica por ×1,25 pero las tiradas de
                        Supervivencia y Percepción para recolectar materiales y detectar
                        peligros se hacen en Desventaja y al final de la jornada de viaje
                        los personajes y sus monturas ganan 1 Nivel de Fatiga.
                    </p>

                    <p>
                        Si el ritmo de la marcha es <strong>Marcha forzada</strong> la
                        cantidad de km que te desplazas se multiplica por ×1,5 pero las
                        tiradas de Percepción y Supervivencia para detectar peligros se
                        hacen en Desventaja, al final de la jornada de viaje los personajes
                        y sus monturas ganan 2 Niveles de Fatiga y no pueden recolectarse
                        materiales.
