                    <p>
                        <strong>Tutor Generalista</strong>: Son los tutores más comunes y
                        los más baratos. Seguramente solo sepan enseñar algunos Movimientos
                        con bajo coste de PP y que no sigan una temática. El precio sugerido
                        que podrían cobrarte por los Movimientos de 1 PP es de 2.000$, los
                        de 2 PP, 4.000$ y los de 3 PP, 10.000$.
                    </p>

                    <p>
                        <strong>Tutor de Tipo Elemental</strong>: Ciertos tutores enseñan
                        Movimientos de uno o unos pocos tipos elementales en exclusiva.
                        Pueden enseñar Movimientos de 1 PP como máximo, 3 PP si es un tutor
                        excepcional y 4 PP si es uno de los mejores tutores que hay. El
                        precio de los servicios de estos tutores crecerá en función de que
                        tan buenos sean, pero una buena pauta es que los Movimientos de 1 PP
                        podrían costar 2.000$, los de 2 PP, 5.000$ y los de 3 PP, 10.000$.
                    </p>

                    <p>
                        <strong>Tutor de Especialidad</strong>: Algunos tutores enseñarán
                        sólo Movimientos de un cierto tipo. Por ejemplo, Movimientos con una
                        cierta Etiqueta o que tienen una cierta temática. Estos tutores
                        usualmente son más raros que los tutores de Tipo Elemental y, al ser
                        más especializados, sus servicios son más caros, Esta
                        especialización suele indicar que sabrá enseñar casi todos los
                        Movimientos dentro de su temática. El precio sugerido que podrían
                        cobrarte por los Movimientos de 1 PP es de 3.000$, los de 2 PP,
                        6.000$ y los de 3 PP, 12.000$.
                    </p>

                    <p>
                        <strong>Tutor de Movimiento Mítico</strong>: Estos tutores serán
                        extremadamente raros y sólo enseñarán unos muy pocos Movimientos
                        especialmente poderosos, como los Movimientos de 4 PP. Para los
                        costes de los servicios de estos tutores considera poner precios muy
                        prohibitivos, como 20.000$.
