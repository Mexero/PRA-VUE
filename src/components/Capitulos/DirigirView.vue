                <summary class="subSummary">Puntos de Superioridad</summary>
                <div class="detailsBloque">
                    <p>
                        Puntos de Superioridad En ciertas ocasiones, como por ejemplo cuando
                        hay un Entrenador o Pokémon muy superior al grupo de los PJs o
                        cuando los PJs enfrenten a una criatura bajo un efecto que la
                        potencia tanto que la hace perder el control, querrás que dicho
                        Entrenador o Pokémon se sienta mucho más poderoso que los jugadores.
                        Podrías recurrir a darles más nivel, pero esto produciría una
                        situación en la que los jugadores serían arrasados, siendo incapaces
                        de afectar a los Pokémon rivales y estos, a su vez, haciendo
                        muchísimo daño a los jugadores. En lugar de esto, cuando quieras
                        crear Entrenadores o Pokémon que se sientan mucho más poderosos sin
                        ser injustos, deberías hacer uso de las reglas de
                        <strong>Puntos de Superioridad</strong>.
                    </p>
                    <p>
                        Los Puntos de Superioridad son un recurso que pueden tener las
                        criaturas que controla el Director de Juego y que tienen varios usos
                        y efectos sobre estas:
                    </p>

                    <p>
                        <strong>Mejora de Resiliencia</strong>. Una criatura que empieza un
                        combate con Puntos de Superioridad multiplica sus PV y sus PP
                        máximos por su cantidad de Puntos de Superioridad más 1
                    </p>

                    <p>
                        <strong> Acciones de Superioridad</strong>. Una criatura con Puntos
                        de Superioridad puede tomar un Turno de Superioridad al final de
                        cada uno de los turnos de otras Criaturas rivales en la Iniciativa.
                        La cantidad máxima de Turnos de Superioridad que puede tomar una
                        Criatura por ronda es igual a la cantidad de Puntos de Superioridad
                        que tiene.
                    </p>

                    <p>
                        Los <strong>Turnos de Superioridad</strong> son turnos en los que
                        sólo pueden tomarse una Acción o Acción compartida y en los que la
                        criatura puede moverse una cantidad de metros igual a la mitad de su
                        Velocidad.
                    </p>

                    <p>
                        <strong>Evitar Efectos</strong>. Una criatura puede gastar un Punto
                        de Superioridad para liberarse de un Estado o ignorar el efecto de
                        un Movimiento, Rasgo o Habilidad. Una vez un Punto de Superioridad
                        se gasta de esta forma, este no se recupera hasta que la criatura
                        Descanse y, por lo tanto, la criatura pierde una de sus Acciones de
                        Superioridad.
                    </p>

                    <p>
                        <strong>Movimientos Extra</strong>. La criatura puede conocer varios
                        Movimientos Enseñables, los cuales podría no olvidar si pierde los
                        Puntos de Superioridad (tu elección como DJ cuando le das dichos
                        Movimientos).
                    </p>

                    <p>
                        <strong>Efectos Extra</strong>. Si quieres hacer que un Pokémon con
                        Puntos de Superioridad se sienta de una forma determinada, como, por
                        ejemplo, que es muy resistente, muy ágil o poderoso puedes darle uno
                        o varios de los siguientes efectos:
                    </p>
                    <ul>
                        <li>
                            <p>
                                La criatura gana Puntos de Escudo igual a su cantidad de Puntos
                                de Superioridad multiplicada por la Mitad de su Nivel al
                                principio de cada uno de sus turnos.
                            </p>
                        </li>
                        <li>
                            <p>
                                La criatura puede usar una Acción de Superioridad para moverse
                                toda su velocidad, provocando o no ataques de oportunidad (tu
                                elección cuando le otorgas esta capacidad).
                            </p>
                        </li>
                        <li>
                            <p>
                                La criatura añade un dado al daño al Daño Base de todos sus
                                Movimientos.
                            </p>
                        </li>
                    </ul>

                    <p>
                        Por cada Efecto Extra que le otorgas a un Pokémon, este puede tomar
                        una Acción de Superioridad menos en cada ronda, a no ser que el
                        efecto ya consuma una.
                    </p>

                    <div class="consejoCapitulo">
                        <p>
                            <strong>Nota</strong>: Considera que, si das Puntos de
                            Superioridad a un Entrenador que quieres que sólo use un Pokémon a
                            la vez, tienes que darle la misma cantidad de Puntos de
                            Superioridad a todos sus Pokémon. De lo contrario, estos no
                            podrían seguir el ritmo de las Órdenes que éste les comanda.
                        </p>
                    </div>

                    <p>
                        <strong>Poder de Puntos de Superioridad</strong>. Una criatura con
                        Puntos de Superioridad cuenta como una cantidad de criaturas
                        equivalentes iguales a sus Puntos de Superioridad más 1. Por lo
                        tanto, si quieres que una criatura pueda rivalizar con 1 Pokémon de
                        cada uno de tus jugadores deberás darle una cantidad de Puntos de
                        Superioridad igual a tu cantidad de jugadores menos 1 y, si quieres
                        que equivalga a dos Pokémon de estos, deberás de darle el doble de
                        esos Puntos de Superioridad.
                    </p>

                    <p>
                        <strong>Capturar Pokémon con Puntos de Superioridad</strong>. En
                        muchas ocasiones, capturar un Pokémon que posee Puntos de
                        Superioridad será imposible, al menos, hasta que se quede sin estos
                        Puntos de Superioridad. Si, por algún motivo, un Entrenador consigue
                        capturar un Pokémon con Puntos de Superioridad, este los pierde
                        automáticamente, junto a todos los efectos que tenerlos le
                        otorgasen.
                    </p>

                    <p>
                        <strong>De uno en uno</strong>. La mayor parte de las veces que un
                        grupo enfrente a una criatura con Puntos de Superioridad lo harán en
                        solitario, puesto que estas ya son lo suficientemente poderosas como
                        para plantar cara al grupo y, en general, no deberías utilizar
                        múltiples entrenadores y Pokémon que no son de Entrenadores que
                        tengan Puntos de Superioridad en un mismo combate, puesto que esto
                        haría el combate imposible de ganar y muy lento.
                    </p>
                </div>
            </details>

