                        jornada. Hay cuatro ritmos: Lento, Medio, Rápido y Marcha forzada.
                    </p>

                    <p>
                        Si el ritmo de la marcha es <strong>Medio</strong>, la cantidad de
                        km que te desplazas no cambia y puedes recolectar materiales como
                        plantas o agua una vez por día mediante una Tirada de Supervivencia
                        (Espíritu).
                    </p>

                    <p>
                        Si el ritmo de la marcha es <strong>Lento</strong>, la cantidad de
                        km que te desplazas es multiplicada por ×0,75, pero tienes Ventaja
                        en Tiradas de Supervivencia y Percepción para recolectar materiales
                        y detectar peligros en el camino.
                    </p>

