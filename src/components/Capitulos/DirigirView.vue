    <details open id="apartado4">
        <summary>Diseña Sesiones</summary>
        <div class="detailsBloque">
            <p>
                Los arcos de una campaña estarán subdivididos en sesiones, las cuales
                deberán replicar la estructura de los arcos. Es decir, cada una de estas
                deberá tener uno o varios objetivos a alcanzar o conflictos a resolver.
                Es probable que en una sesión no ocurran todas las situaciones que has
                planteado, pero esto no está mal siempre y cuando en esta se hayan
                cumplido los siguientes tres principios:
            </p>

            <p>
                <i> Las sesiones deben centrarse en la <strong>interacción</strong></i>: Puede que tengas una
                gran historia planteada en tu cabeza o que tengas preparada una
                narración muy buena para un evento, pero, aunque esto es bueno, una
                buena sesión de rol no se caracteriza por esto, sino porque los
                jugadores sientan que sus PJs pueden interactuar con el mundo de forma
                activa, permitiéndoles tomar sus propias decisiones, actuar como decidan
                y lidiar con las consecuencias, positivas o negativas, de sus acciones.
            </p>

            <p>
                También será muy importante promover la interacción entre personajes,
                planteando tareas que cada uno de los personajes sea experto en resolver
                y que, por lo tanto, todos puedan aportar algo al trabajar
                conjuntamente.
            </p>

            <p>
                <i>Las sesiones deben ser <strong>divertidas</strong> por si solas</i>: Una sesión en la que los
                jugadores se aburren en pos de hacer tareas tediosas para, en la
                siguiente, hacer algo muy divertido, es una mala sesión. Cada sesión que
                juegues debe ser divertida para todos tus jugadores. Esto no implica que
                en una sesión todos los jugadores deban tener el mismo protagonismo,
                pero sí que todos deben poder aportar a las interacciones y tener formas
                de tomar decisiones y actuar por sí mismos.
            </p>

            <p>
                <i>Las sesiones deben <strong>progresar la trama</strong> de la campaña</i> No toda sesión
                tiene que resolver un gran conflicto ni contener un punto de inflexión
                en la trama, pero todas deben aportar algo a la progresión de la
                campaña. Es decir, al final de la sesión debe de poder decirse que se
                tiene ahora que no se tenía al principio de la misma y cómo ha cambiado
                la situación del grupo. Por ejemplo, en una sesión el grupo puede estar
                preparándose para luchar contra un enemigo muy poderoso que usa Pokémon
