                    <p>
                        El <strong>Rango de Efecto Secundario</strong> de un Movimiento es
                        el rango de la Tirada Natural en la que un efecto ocurre, ya sea una
                        Tirada de Ataque o de Salvación. Este rango es igual al Coste en PP
                        del Movimiento (mínimo 2). Si el movimiento afecta un área este
                        rango disminuye en 1 si es de 0 o 1 PP o en 2 si es de 3 o 4 PP. En
                        Tiradas de Salvación, esto significa que la Tirada Natural sea
                        inferior o igual al Rango de Efecto Secundario y en Tiradas de
                        Ataque, que esta sea superior o igual a 20 menos el Rango de Efecto
                        Secundario.
                    </p>

                    <p>
                        Los <strong>Efectos secundarios</strong> que puede provocar un
                        Movimiento se clasifican en función de su poder:
                    </p>

                    <p>
                        <strong>Poco relevantes</strong>: Son efectos que muchas veces no
                        hacen demasiado, como dejar a una criatura Empapada. Para estos, el
                        Rango de Efecto Secundario aumenta en 1
                    </p>

                    <p>
                        <strong>Básicos</strong>: Mayor rango de crítico, aplicar un estado
                        que no sea apresar, infatuar o dormir hasta el final de la Escena,
                        infatuar hasta el final de la escena, empujar 1 metro, dejar
                        tumbado, moverse 2 metros, dejar de provocar ataques de oportunidad
                        hasta el final del turno o tener un dos efectos cuando la Tirada
                        Natural asociada al Movimiento esté en el Rango de Efecto secundario
                        del Movimiento.
                    </p>

                    <p>
                        <strong>Fuertes</strong>: Aplicar 2 efectos Básicos o el doble de
                        alguno de estos, hacer crítico en el Rango de Efecto Secundario,
                        cegar hasta el final del turno, infatuar hasta el final de la
                        Escena, prohibir el uso de Órdenes o Movimientos de cierto tipo
                        hasta el final del turno. Estos efectos quedan reservados sólo para
                        Movimientos que sólo afectan a una criatura, hacen daño de
                        Movimientos como si afectasen un área y, además, el Rango de Efecto
                        Secundario se reduce en 1.
                    </p>

                    <p>
                        Además, también existen efectos secundarios muy poderosos que
                        alteran completamente el Movimiento. Por ejemplo:
                    </p>

                    <p>
                        Los Movimientos que hacen daño y restauran la mitad de ese daño no
                        añaden EST al daño nunca y deben ser de un sólo objetivo.
                    </p>

                    <p>
                        Los Movimientos que apresan o duermen hasta el final del turno sólo
                        afectan a un objetivo y hacen daño como si fuesen un Movimiento que
                        hace daño en área y tuviera un coste en PP inferior en 1.
                    </p>

                    <p>
                        Por último, si un Movimiento tiene la Etiqueta de Retroceso o de
                        Explosión, lanza d12 en lugar del tipo de dado que lanzase y, si
                        cuesta 1 PP, hace daño igual 3d12 + EST en lugar de 2d10 + EST.
