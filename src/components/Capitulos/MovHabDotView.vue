        Dotes
    </summary>
    <div class="detailsBloque">
        <p>Previo al inicio de la campaña, tu DJ establecerá si
            en esta utilizareis las Dotes. Estas son Rasgos
            extra que tanto Entrenadores como Pokémon
            ganan a ciertos niveles que permiten personalizar
            más a entrenadores y Pokémon y que pueden estar
            enfocadas en el Combate o en otras actividades
            fueras de este, como la exploración, la interacción
            social o la fabricación de Objetos.</p>
        <p>Si no usas las Dotes, simplemente ignóralas, si usas
            las Dotes, estas sólo pueden tomarse una vez, a no
            ser que estas sean Repetibles, lo cual se indica en
            la información de estas. </p>
        <p>Algunas Dotes tienen prerrequisitos. Es decir, que
            sólo las Criaturas que cumplen ciertas condiciones
            pueden tomarlas. Una Criatura que deja de cumplir
            los prerrequisitos de una Dote la pierde
            automáticamente y puede tomar otra Dote que
            pudiera haber tomado en el momento en el que
            tomó dicha Dote. Por ejemplo, si dejas de calificar
            para una Dote que tomaste a Nivel 3, no puedes
            cambiarla por una Dote que requiere que seas de
            Nivel 9, incluso si tu nivel ahora es de 9 o más.
        </p>
        <p>Si de alguna forma puedes cambiar una de tus
            Dotes por otra, sólo puedes cambiarla por una que
            pudieras tomar en el Momento en el que la
            ganaste.</p>
