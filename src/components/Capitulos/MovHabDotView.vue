        <p>Si un Movimiento tiene efecto sobre un área, el
            tamaño y forma del área se escriben como Forma
            (Dimensiones en metros). El área se origina de ti, a
            no ser que se declare un rango para este área, en
            cuyo caso el punto de origen debe de estar dentro
            de este área. Por ejemplo, Radio (2 m), indica que
            el Movimiento afecta un radio de 2 metros a tu
            alrededor y el rango 4 m / Radio (2 m) indica que
            el Movimiento afecta un radio de 2 metros
            originado desde un punto a 4 metros de distancia
            de ti. Además, la dimensión descrita dentro de un
            Movimiento hace referencia a diferentes valores
            que describen la forma. En el caso del Radio, esta
            representa la distancia que este mide, en el Cubo,
            la dimensión del lado del cubo, en el caso del
            Cilindro, la primera dimensión representa el radio y
            la segunda la altura, en el Cono, la distancia dada
            representa tanto la altura como la base del mismo
            y, en el caso de la Línea, la primera la longitud y la
            última el grosor (diámetro).</p>

        <p>En la segunda fila de información de la plantilla de
            Movimientos hay 3 valores que no pueden
            aparecer. Si lo hacen, significa que el Movimiento
            no tiene ese valor. Estos valores son Daño,
            Estadísticas asociadas y Etiquetas. </p>


        <p>El <strong>Daño</strong> o <strong>Daño base</strong> de un Movimiento se refiere a
            la cantidad de daño que se inflige cuando su
            descripción dice que lo hace. </p>

        <p>Usualmente, el Daño de un Movimiento viene dado
            por una fórmula que hace referencia a cantidad de
            dados más un bono de daño dependiente de una o
            varias Estadísticas. Por ejemplo, si el daño de un
            Movimiento es 1d8 + FUE, el daño que este realiza
            es de 1d8 más su Fuerza</p>

        <p>Además, si un Movimiento hace daño a múltiples
            Criaturas en un área, el daño se lanza una sola vez
            y es el mismo para todas las criaturas afectadas.
        </p>

        <p>Las <strong>Etiquetas</strong> de un Movimiento son un conjunto de
            categorías que ayudan a clasificar los Movimientos
            a la hora de que ciertos efectos los modifiquen. Por
            ejemplo, la habilidad Mandíbula Fuerte potencia
            los Movimientos con la Etiqueta de Mordisco. La
            mayoría de Etiquetas son autoexplicativas pero
            más abajo hay un apartado que las comenta y da
            sus reglas especiales, si las tienen</p>

        <p>Las <strong>Estadísticas Asociadas</strong> a un Movimiento es una
            Estadística que puede elegir una Criatura para
            usar un Movimiento de entre la lista ofrecida por el
            mismo. Si un valor asociado a un Movimiento
            depende de esta Estadística asociada, esta dice
            que depende de su EST. </p>

        <p>Si un Movimiento requiere realizar una
            <strong>Tirada de Ataque</strong> para que este tome
            efecto, la Criatura que lo usa se considera
            Buena en la Tirada de Ataque y añade a
            esta la Estadística Asociada que elige. Es
            decir, que su bono a la Tirada de Ataque es
            igual a la EST + BH.
        </p>

        <p><strong>Todos los Movimientos tienen un CD</strong>, el
            cual es el valor que deben igualar o
            superar las Criaturas al hacer Tiradas de
            Salvación contra el Movimiento si este
            requiere que se hagan para evitar su
            efecto. Este CD se calcula como 10 + BH +
            EST.</p>

        <p>La <strong>Descripción</strong> de un Movimiento explica lo que
            ocurre al usarlo y las Tiradas de d20 que este
            requiere hacer y los efectos de estas. Si un
            Movimiento hace daño y provoca otro efecto, este
            segundo se denomina su <strong>Efecto Secundario</strong>.</p>

        <p>Los Movimientos que cuestan una Reacción tienen
