        Mejorar el daño base
    </summary>
    <div class="detailsBloque">
        <p>El Daño base de los Movimientos puede ser
            aumentado. Hay dos formas principales de que
            esto ocurra: Que se añada un bono de daño fijo o
            que se añadan dados al daño base</p>

        <p>Los efectos que dicen que <strong>añades un dado al daño
                base</strong> añaden un dado del mismo tamaño que ya
            lanza el movimiento. Es decir, si el movimiento
            lanza d6, añades 1d6, si lanza d10, añades 1d10.</p>

        <p>Si un efecto <strong>añade un bono fijo</strong>, este simplemente
            se añade. Sin embargo, si dos o más efectos
            añaden un bono al daño base de un Movimiento
            que depende del BH del usuario no se suman
            ambos, sino que se toma sólo el mayor.</p>

        <p>Además, si un Movimiento hace <strong>daño en múltiples
                instancias</strong>, por ejemplo, cuando hace dos Tiradas
            de Ataque por separado, los bonos al daño de un
            Movimiento sólo aplican a la primera instancia de
            daño que el Movimiento provoque.</p>
    </div>
</details>

