    </summary>
    <div class="detailsBloque">
        <p>Todas las especies Pokémon tienen sus
            particularidades. Para representar estas
            capacidades únicas y especiales todas las especies
            Pokémon tienen dos <strong>Habilidades</strong> y una o dos
            <strong>Habilidades Ocultas</strong>.
        </p>
        <p>Las Habilidades son las capacidades que tienen
            todos los miembros de su especie y la Habilidades
            Ocultas capacidades que sólo unos pocos de esta
            poseen y la cual puede obtenerse de diferentes
            formas a medida en que los entrenadores y
            Pokémon suben de nivel. </p>
        <p>Además, algunos Rasgos de Clase permiten
            enseñar ciertas Habilidades a los Pokémon,
            aunque estas son poco comunes. Por otro lado, los
            entrenadores no poseen Habilidades de forma
            natural, pero pueden acceder a las mismas
            mediante Dotes y Rasgos de Clase.
        </p>
        <p>Algunas Habilidades hacen uso de la notación EST
            de la misma forma que los Movimientos. Cuando
            EST aparece en una Habilidad significa que puedes
            elegir entre varias Estadísticas para usarla. En el
            Capítulo 12: Habilidades, encontrarás una lista que
            contiene todas las Habilidades.</p>

